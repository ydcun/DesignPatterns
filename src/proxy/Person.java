package proxy;

public class Person {
	private String name;
	public Person(String name) {
		this.name = name;
	}
	/**  
	 * 获取  name  
	 * @return name name  
	 */
	public String getName() {
		return name;
	}
	/**  
	 * 设置  name  
	 * @param name name  
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	
}
